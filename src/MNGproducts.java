    public void fetchproducts()
    {
        ResultSet rs=DBLoader.executeSql("select * from pos.products");
        try {
            while(rs.next())
            {
                String name=rs.getString("name");
                String cat=rs.getString("cat");
                String price =rs.getString("price");
                String qty = rs.getString("qty");
                String photo1 = rs.getString("photo1");
                
                al.add(new product_bean(name, cat, price, qty, photo1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(MNGproducts.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
      class mytablemodel extends AbstractTableModel{

        @Override
        public int getRowCount() {
            return al.size();
        }

        @Override
        public int getColumnCount() {
            return 5;
        }
        
        
        @Override
        public Object getValueAt(int i, int j) {
            product_bean p=al.get(i);
            if(j==0)
            {
                return p.name;
            }
            if(j==1)
            {
               return p.cat; 
            }
            if(j==2)
            {
                return p.price;
                
            }
            if(j==3)
            {
                return p.qty;
            }
            else
            {
                return p.photo1;
            }
        }
        
                @Override
        public String getColumnName(int column) {
            if (column == 0) return "Name";
            else if (column == 1) return "Category";
            else if (column == 2) return "Price";
            else if (column == 3) return "Quantity";
            else return "Photo";
        }
        
    }


