    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText().trim();
String category = cb1.getSelectedItem().toString();
String price = jTextField2.getText().trim();   // make sure this exists
String qty = jTextField3.getText().trim();     // make sure this exists

// validation
if (name.isEmpty() || price.isEmpty() || qty.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Please fill all fields");
    return;
}

if (selectedfile == null) {
    JOptionPane.showMessageDialog(this, "Please select image first");
    return;
}

try {

    // save image
    String photoname = FileUtil.savePhoto(selectedfile);

    ResultSet rs = DBLoader.executeSql(
        "SELECT * FROM products WHERE name='" + name + "'"
    );

    if (rs.next()) {
        JOptionPane.showMessageDialog(this, "Product already exists");
    } 
    else {
        
        rs.moveToInsertRow();
        
        rs.updateString("name", name);
        rs.updateString("cat", category);        
        rs.updateString("price", price);
        rs.updateString("qty", qty);           
        rs.updateString("photo1", "src/myuploads/" + photoname); 
        rs.insertRow();

        JOptionPane.showMessageDialog(this, "Product added successfully");

        // ✅ update table
        al.add(new product_bean(name, category, price, qty, "src/myuploads/" + photoname));
        tm.fireTableDataChanged();

        // clear fields
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jLabel6.setIcon(null);
        selectedfile = null;
    }

} catch (Exception e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
}
    }//GEN-LAST:event_jButton2ActionPerformed

